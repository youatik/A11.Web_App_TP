package servlets;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;
import java.io.IOException;

/**
 *
 * @author youatik
 */


public class LogoutServlet extends HttpServlet {
    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        HttpSession session = request.getSession(false); // Retrieve the existing session if it exists

        if (session != null) {
            session.invalidate(); // Invalidate the session, removing all session attributes
        }

        response.sendRedirect("logout.jsp"); // Redirect to the logout page after successful logout
    }
}
